function toCamelCase(str) {
  return str
    .replace(/[-_\s/]+(.)/g, (_, c) => c.toUpperCase())
    .replace(/^[A-Z]/, c => c.toLowerCase());
}

function toSemanticName(operationId, method, path) {
  if (operationId) return toCamelCase(operationId);
  const verbMap = {
    get: 'get', post: 'create', put: 'update',
    patch: 'patch', delete: 'delete', head: 'head', options: 'options',
  };
  const verb = verbMap[method.toLowerCase()] || method.toLowerCase();
  const parts = path
    .split('/')
    .filter(Boolean)
    .map(p => {
      if (p.startsWith('{') && p.endsWith('}')) {
        const param = p.slice(1, -1);
        return 'By' + param[0].toUpperCase() + param.slice(1);
      }
      return p[0].toUpperCase() + p.slice(1);
    });
  return verb + parts.join('');
}

function sanitizeIdentifier(str) {
  return str.replace(/[^a-zA-Z0-9_$]/g, '_').replace(/^(\d)/, '_$1');
}

export function sanitizeTagForFilename(tag) {
  return tag.toLowerCase().replace(/[^a-z0-9-]/g, '-').replace(/-+/g, '-').replace(/^-|-$/g, '') || 'untagged';
}

