async function fetchWithRetry(url, attempt = 0) {
  try {
    const res = await fetch(url);
    if (!res.ok) throw new Error(`HTTP ${res.status}: ${res.statusText}`);
    return await res.text();
  } catch (err) {
    if (attempt === 0) {
      const delay = 1000;
      console.error(`  Fetch failed (${err.message}), retrying in ${delay}msвЂ¦`);
      await new Promise(r => setTimeout(r, delay));
      return fetchWithRetry(url, 1);
    }
    throw err;
  }
}

async function loadRawSpec(specArg) {
  if (specArg.startsWith('http://') || specArg.startsWith('https://')) {
    return fetchWithRetry(specArg);
  }
  const absPath = resolve(specArg);
  if (!existsSync(absPath)) {
    console.error(`Error: spec file not found: ${absPath}`);
    process.exit(1);
  }
  return readFileSync(absPath, 'utf8');
}

