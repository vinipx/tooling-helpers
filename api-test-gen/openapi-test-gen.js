function generateEndpointsFile(endpoints) {
  const entries = endpoints.map(({ name, method, path }) =>
    `  ${name}: { method: '${method.toUpperCase()}', path: '${path}' }`
  );
  return [
    `// endpoints.js вЂ” auto-generated by openapi-test-gen`,
    ``,
    `export const ENDPOINTS = Object.freeze({`,
    entries.join(',\n'),
    `});`,
    ``,
  ].join('\n');
}

function generateConfigFile(baseUrl) {
  return [
    `// config.js вЂ” auto-generated by openapi-test-gen`,
    `// Override BASE_URL at runtime via the BASE_URL environment variable`,
    `// without needing to regenerate tests (e.g. BASE_URL=https://staging.api.com npm test)`,
    ``,
    `export const BASE_URL = process.env.BASE_URL ?? '${baseUrl.replace(/\/$/, '')}';`,
    ``,
  ].join('\n');
}

function generateValidatorsFile(schemas) {
  const header = [
    `// validators.js вЂ” auto-generated by openapi-test-gen`,
    ``,
    `import Ajv from 'ajv';`,
    `import addFormats from 'ajv-formats';`,
    ``,
    `const ajv = new Ajv({ allErrors: true });`,
    `addFormats(ajv);`,
    ``,
  ];

  if (schemas.length === 0) {
    header.push(`// No 2xx response schemas found in spec.`);
    header.push(``);
    return header.join('\n');
  }

  const body = [];
  for (const { name, schema } of schemas) {
    body.push(`export const ${name} = ajv.compile(${safeStringify(schema, 2)});`);
    body.push(``);
  }

  return [...header, ...body].join('\n');
}

function generateTestFile(tag, endpoints, authHeader, fwConfig) {
  const filename = `${sanitizeTagForFilename(tag)}.test.js`;
  const lines = [
    `// ${filename} вЂ” auto-generated by openapi-test-gen`,
    ``,
    fwConfig.testImportLine,
    `import { BASE_URL } from './config.js';`,
    `import { ENDPOINTS } from './endpoints.js';`,
    `import * as validators from './validators.js';`,
    ``,
  ];

  if (authHeader) {
    const colonIdx = authHeader.indexOf(':');
    const headerName = authHeader.slice(0, colonIdx).trim();
    const headerValue = authHeader.slice(colonIdx + 1).trim();
    lines.push(`const AUTH_HEADERS = { '${headerName}': '${headerValue}' };`);
    lines.push(``);
  }

  for (const { name, method, path, pathParams, queryParams, bodyFields, responses, operationId } of endpoints) {
    const describeName = (operationId || `${method.toUpperCase()} ${path}`).replace(/'/g, "\\'");

    // Build URL using ENDPOINTS and BASE_URL вЂ” path params replaced with PLACEHOLDER_ values
    let endpointPath = `ENDPOINTS.${name}.path`;
    const pathSubstitutions = pathParams.map(p =>
      `.replace('{${p}}', 'PLACEHOLDER_${p.toUpperCase()}')`
    ).join('');
    const urlExpression = `\`\${BASE_URL}\${${endpointPath}${pathSubstitutions}}\``;

    // Find first 2xx validator
    let validatorName = null;
    for (const [status, info] of Object.entries(responses)) {
      if (/^2\d\d$/.test(status) && info.validatorName) {
        validatorName = info.validatorName;
        break;
      }
    }

    // Build TODO hint
    const todoItems = [];
    if (pathParams.length) todoItems.push(`Path params: ${pathParams.join(', ')}`);
    if (queryParams.length) todoItems.push(`Query params: ${queryParams.join(', ')}`);
    if (bodyFields.length) todoItems.push(`Body fields: ${bodyFields.join(', ')}`);

    lines.push(`describe('${describeName}', () => {`);
    if (todoItems.length) {
      lines.push(`  // TODO: ${todoItems.join(' | ')}`);
    }
    lines.push(`  it('returns 2xx on happy path', async () => {`);
    lines.push(`    const response = await fetch(${urlExpression}, {`);
    lines.push(`      method: '${method.toUpperCase()}',`);
    if (authHeader) {
      lines.push(`      headers: AUTH_HEADERS,`);
    }
    lines.push(`    });`);
    lines.push(`    expect(response.ok).toBe(true);`);
    if (validatorName) {
      lines.push(`    const body = await response.json();`);
      lines.push(`    validators.${validatorName}(body);`);
      lines.push(`    expect(validators.${validatorName}.errors).toBeNull();`);
    }
    lines.push(`  });`);
    lines.push(`});`);
    lines.push(``);
  }

  return lines.join('\n');
}

